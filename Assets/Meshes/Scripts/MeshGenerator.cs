    class CubeMesh
    {
        GameMap.Grid3i grid;
        public List<Vector3> vertexList = new List<Vector3>();
        public List<Vector3> normalList = new List<Vector3>();
        public List<int> triangleList = new List<int>();
        int width, height, depth;
        public CubeMesh(ref GameMap.Map map)
        {
            grid = map.GetCubeGrid();
            width = grid.width; height = grid.height; depth = grid.depth;
            GetCubeVertices();
            GetCubeTriangles();
            GetCubeNormals();
        }

        void GetCubeNormals()
        {
            for (int i = 0; i < normalList.Count; i++)
            {
                normalList[i].Normalize();
            }
        }

        void GetCubeVertices() //should be private, made public for testing
        {
            Vector3 tempVec = Vector3.zero;
            bool vecNeeded;
            int val;
            List<int> comp = new List<int>();
            for (int i = 0; i < width - 1; i++)
            {
                for (int f = 0; f < height - 1; f++)
                {
                    for (int g = 0; g < depth - 1; g++)
                    {
                        tempVec.x = i + 1;
                        tempVec.y = f + 1;
                        tempVec.z = g + 1;
                        vecNeeded = false;
                        comp.Clear();
                        for (int h = 0; h < 2; h++)
                        {
                            for (int j = 0; j < 2; j++)
                            {
                                for (int k = 0; k < 2; k++)
                                {
                                    val = grid.Get(i + h, f + j, g + k);
                                    if (!comp.Contains(val))
                                    {
                                        comp.Add(val);
                                    }
                                    if (comp.Count > 1) { vecNeeded = true; break; }
                                }
                                if (vecNeeded) { break; }
                            }
                            if (vecNeeded) { break; }
                        }
                        if (vecNeeded)
                        {
                            vertexList.Add(tempVec);
                            normalList.Add(Vector3.zero);
                        }
                    }
                }
            }
            /*foreach (Vector3 item in output)
            {
                Debug.Log(item);
            }*/
        }

        int GetIndex(Vector3 vec)
        {
            for (int i = 0; i < vertexList.Count; i++)
            {
                if (vertexList[i] == vec) { return i; }
            }
            return -1;
        }

        void AddQuad(Vector3[] quad, int[] quadInts)
        {
            quadInts[0] = GetIndex(quad[0]);
            quadInts[1] = GetIndex(quad[1]);
            quadInts[2] = GetIndex(quad[2]);
            quadInts[3] = GetIndex(quad[3]);
            triangleList.Add(quadInts[0]);
            triangleList.Add(quadInts[1]);
            triangleList.Add(quadInts[2]);
            triangleList.Add(quadInts[1]);
            triangleList.Add(quadInts[3]);
            triangleList.Add(quadInts[2]);
            Vector3 normal = Vector3.Cross(quad[1] - quad[0], quad[2] - quad[0]);
            foreach (int index in quadInts)
            {
                normalList[index] += normal;
            }
        }

        void MakeFaces(int x, int y, int z)
        {
            Vector3[] quad = new Vector3[4];
            int[] quadInts = new int[4];
            if (grid.Check(x - 1, y, z, 1))//left
            {
                quad[0].x = x; quad[0].y = y; quad[0].z = z;
                quad[1].x = x; quad[1].y = y; quad[1].z = z + 1;
                quad[2].x = x; quad[2].y = y + 1; quad[2].z = z;
                quad[3].x = x; quad[3].y = y + 1; quad[3].z = z + 1;
                AddQuad(quad, quadInts);
            }
            if (grid.Check(x + 1, y, z, 1))//right
            {
                quad[0].x = x + 1; quad[0].y = y; quad[0].z = z;
                quad[1].x = x + 1; quad[1].y = y + 1; quad[1].z = z;
                quad[2].x = x + 1; quad[2].y = y; quad[2].z = z + 1;
                quad[3].x = x + 1; quad[3].y = y + 1; quad[3].z = z + 1;
                AddQuad(quad, quadInts);
            }
            if (grid.Check(x, y + 1, z, 1))//bottom
            {
                quad[0].x = x; quad[0].y = y + 1; quad[0].z = z;
                quad[1].x = x; quad[1].y = y + 1; quad[1].z = z + 1;
                quad[2].x = x + 1; quad[2].y = y + 1; quad[2].z = z;
                quad[3].x = x + 1; quad[3].y = y + 1; quad[3].z = z + 1;
                AddQuad(quad, quadInts);
            }

            if (grid.Check(x, y - 1, z, 1))//top
            {
                quad[0].x = x; quad[0].y = y; quad[0].z = z;
                quad[1].x = x + 1; quad[1].y = y; quad[1].z = z;
                quad[2].x = x; quad[2].y = y; quad[2].z = z + 1;
                quad[3].x = x + 1; quad[3].y = y; quad[3].z = z + 1;
                AddQuad(quad, quadInts);
            }

            if (grid.Check(x, y, z + 1, 1))//front
            {
                quad[0].x = x; quad[0].y = y; quad[0].z = z + 1;
                quad[1].x = x + 1; quad[1].y = y; quad[1].z = z + 1;
                quad[2].x = x; quad[2].y = y + 1; quad[2].z = z + 1;
                quad[3].x = x + 1; quad[3].y = y + 1; quad[3].z = z + 1;
                AddQuad(quad, quadInts);
            }

            if (grid.Check(x, y, z - 1, 1))//back
            {
                quad[0].x = x; quad[0].y = y; quad[0].z = z;
                quad[1].x = x; quad[1].y = y + 1; quad[1].z = z;
                quad[2].x = x + 1; quad[2].y = y; quad[2].z = z;
                quad[3].x = x + 1; quad[3].y = y + 1; quad[3].z = z;
                AddQuad(quad, quadInts);
            }

        }

        void GetCubeTriangles()
        {
            Mesh cubeMesh = new Mesh();
            List<int> triangleList = new List<int>();

            cubeMesh.vertices = vertexList.ToArray();

            for (int i = 0; i < width; i++)
            {
                for (int f = 0; f < height; f++)
                {
                    for (int g = 0; g < depth; g++)
                    {
                        if (grid.Check(i, f, g, 0)) { MakeFaces(i, f, g); }
                    }
                }
            }
        }

        public Mesh GetCubeMesh()
        {
            Mesh cubeMesh = new Mesh();
            cubeMesh.vertices = vertexList.ToArray();
            cubeMesh.triangles = triangleList.ToArray();
            cubeMesh.normals = normalList.ToArray();
            return cubeMesh;
        }

