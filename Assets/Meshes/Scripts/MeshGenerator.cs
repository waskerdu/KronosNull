        Vector3[] vertList = new Vector3[12];
        Vector3[] pointList = new Vector3[8];
        float[] val = new float[8];
        public float isoLevel = 0.5f;

        public Mesh GetMarchingMesh( bool invert = false)
        {
            Mesh marchingMesh = new Mesh();
            marchingMesh.vertices = vertexList.ToArray();
            if (invert)
            {
                int save;
                for (int i = 0; i < normalList.Count; i++)
                {
                    normalList[i] *= -1;
                    if (i % 3 == 0)
                    {
                        save = triangleList[i + 1];
                        triangleList[i + 1] = triangleList[i + 2];
                        triangleList[i + 2] = save;
                    }
                }
            }
            marchingMesh.triangles = triangleList.ToArray();
            marchingMesh.normals = normalList.ToArray();
            return marchingMesh;
        }

        public MarchingMesh(ref GameMap.Grid3f _grid)
        {
            grid = _grid;
            width = grid.width; height = grid.height; depth = grid.depth;
            //cubeGrid = map.GetCubeGrid();
            edgeTable = data.edgeTable;
            triTable = data.triTable;
            for (int i = 0; i < width-1; i++)
            {
                for (int f = 0; f < height-1; f++)
                {
                    for (int g = 0; g < depth-1; g++)
                    {
                        Polygonize(i, f, g);
                    }
                }
            }
        }

        public MarchingMesh(ref GameMap.Map map)
        {
            grid = map.GetMarchingGrid();
            width = grid.width; height = grid.height; depth = grid.depth;
            edgeTable = data.edgeTable;
            triTable = data.triTable;
            for (int i = 0; i < width-1; i++)
            {
                for (int f = 0; f < height; f++)
                {
                    for (int g = 0; g < depth-1; g++)
                    {
                        Polygonize(i, f, g);
                    }
                }
            }
        }

        public int Polygonize(int i, int f, int g)
        {
            int cubeIndex = 0;
            //requires specific ordering to play nice with above tables
            pointList[0] = new Vector3(i, f, g + 1);
            pointList[1] = new Vector3(i + 1, f, g + 1);
            pointList[2] = new Vector3(i + 1, f, g);
            pointList[3] = new Vector3(i, f, g);
            pointList[4] = new Vector3(i, f + 1, g + 1);
            pointList[5] = new Vector3(i + 1, f + 1, g + 1);
            pointList[6] = new Vector3(i + 1, f + 1, g);
            pointList[7] = new Vector3(i, f + 1, g);
            val[0] = grid.Get(i, f, g + 1);
            val[1] = grid.Get(i + 1, f, g + 1);
            val[2] = grid.Get(i + 1, f, g);
            val[3] = grid.Get(i, f, g);
            val[4] = grid.Get(i, f + 1, g + 1);
            val[5] = grid.Get(i + 1, f + 1, g + 1);
            val[6] = grid.Get(i + 1, f + 1, g);
            val[7] = grid.Get(i, f + 1, g);
            if ( val[0] < isoLevel) { cubeIndex |= 1; }
            if (val[1] < isoLevel) { cubeIndex |= 2; }
            if (val[2] < isoLevel) { cubeIndex |= 4; }
            if (val[3] < isoLevel) { cubeIndex |= 8; }
            if (val[4] < isoLevel) { cubeIndex |= 16; }
            if (val[5] < isoLevel) { cubeIndex |= 32; }
            if (val[6] < isoLevel) { cubeIndex |= 64; }
            if (val[7] < isoLevel) { cubeIndex |= 128; }
            if (edgeTable[cubeIndex] == 0) { return 0; }

            /* Find the vertices where the surface intersects the cube */
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 1)) { vertList[0] = VertexInterp(pointList[0], pointList[1], val[0], val[1]);}
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 2)) { vertList[1] = VertexInterp(pointList[1], pointList[2], val[1], val[2]); }
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 4)) { vertList[2] = VertexInterp(pointList[2], pointList[3], val[2], val[3]); }
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 8)) { vertList[3] = VertexInterp(pointList[3], pointList[0], val[3], val[0]); }

            if (Convert.ToBoolean(edgeTable[cubeIndex] & 16)) { vertList[4] = VertexInterp(pointList[4], pointList[5], val[4], val[5]); }
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 32)) { vertList[5] = VertexInterp(pointList[5], pointList[6], val[5], val[6]); }
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 64)) { vertList[6] = VertexInterp(pointList[6], pointList[7], val[6], val[7]); }
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 128)) { vertList[7] = VertexInterp(pointList[7], pointList[4], val[7], val[4]); }

            if (Convert.ToBoolean(edgeTable[cubeIndex] & 256)) { vertList[8] = VertexInterp(pointList[0], pointList[4], val[0], val[4]); }
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 512)) { vertList[9] = VertexInterp(pointList[1], pointList[5], val[1], val[5]); }
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 1024)) { vertList[10] = VertexInterp(pointList[2], pointList[6], val[2], val[6]); }
            if (Convert.ToBoolean(edgeTable[cubeIndex] & 2048)) { vertList[11] = VertexInterp(pointList[3], pointList[7], val[3], val[7]); }

            int nTriangle = 0;

            for (int h = 0; triTable[cubeIndex, h] != -1; h+=3)
            {
                //triangleList.Add(vertList[triTable[cubeIndex, h]]);
                Vector3 normal = Vector3.Cross(vertList[triTable[cubeIndex, h + 1]] - vertList[triTable[cubeIndex, h]], vertList[triTable[cubeIndex, h + 2]] - vertList[triTable[cubeIndex, h]]);
                vertexList.Add(vertList[triTable[cubeIndex, h]]);
                triangleList.Add(vertexList.Count - 1);
                vertexList.Add(vertList[triTable[cubeIndex, h + 1]]);
                triangleList.Add(vertexList.Count - 1);
                vertexList.Add(vertList[triTable[cubeIndex, h + 2]]);
                triangleList.Add(vertexList.Count - 1);
                normalList.Add(normal);
                normalList.Add(normal);
                normalList.Add(normal);
            }

            return nTriangle;
        }

        Vector3 VertexInterp(Vector3 p1, Vector3 p2, float valp1, float valp2)
        {
            Vector3 p = Vector3.zero;
            float mu;
            if (Mathf.Abs(isoLevel - valp1) < 0.00001) { return p1; }
            if (Mathf.Abs(isoLevel - valp2) < 0.00001) { return p2; }
            if (Mathf.Abs(valp1-valp2) < 0.00001) { return p1; }

            mu = (isoLevel - valp1) / (valp2 - valp1);
            p.x = p1.x + mu * (p2.x - p1.x);
            p.y = p1.y + mu * (p2.y - p1.y);
            p.z = p1.z + mu * (p2.z - p1.z);

            return p;
        }
    }

